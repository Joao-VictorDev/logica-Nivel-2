# Solicita ao usuário que informe o valor da base do retângulo
print("Qual a base do retângulo:")
base = int(input())  

# Solicita ao usuário que informe o valor da altura do retângulo
print("Qual a altura do retângulo:")
altura = int(input()) 

# Calcula a área do retângulo multiplicando base pela altura
area = base * altura  

# Exibe o resultado do cálculo da área do retângulo
print("A área do retângulo é:", area) 
