# Passo 1: Definir os valores iniciais
A = 10
B = 20

# Passo 2: Usar uma variável temporária para fazer a troca
temp = A  # Armazena o valor de A
A = B     # A recebe o valor de B
B = temp  # B recebe o valor armazenado em temp

# Passo 3: Imprimir os resultados finais
print(A)  # Resultado: 20
print(B)  # Resultado: 10
